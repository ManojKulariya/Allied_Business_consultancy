    <section class="tax-implications py-5">
        <h2>PF Withdrawal and Tax Implications</h2>
        <ul>
            <li>EPF withdrawals are exempt from taxation if an employee consistently contributes for five consecutive years.</li>
            <li>If there's a break in EPF contributions within the five-year period, the withdrawal becomes taxable for that FY.</li>
            <li>TDS (Tax Deducted at Source) is applicable when an employee withdraws an EPF amount exceeding Rs 50,000 before completing five years of service.</li>
            <li>For early EPF withdrawals (before five years) exceeding Rs 50,000:
                <ol>
                    <li>TDS of 10% is deducted if the employee provides their PAN card.</li>
                    <li>Without a PAN card, the TDS rate increases to 20%.</li>
                </ol>
            </li>
            <li>Employees can avoid TDS by submitting Form 15G (for individuals below 60 years) or Form 15H (for individuals above 60 years).</li>
            <li>EPF withdrawals after completing five years of continuous service are tax-free, and no TDS is deducted.</li>
        </ul>
