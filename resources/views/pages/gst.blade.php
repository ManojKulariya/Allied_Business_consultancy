    <section id="what-is-gst-return" class="my-5">
        <h2>What is a GST Return?</h2>
        <p>A GST Return is a detailed financial statement that captures every transaction made by a business during a tax period. It includes details of sales, purchases, taxes collected on sales (output tax), and taxes paid on inputs (input tax). The Government uses this information to calculate your net GST liability and ensure proper flow of Input Tax Credit (ITC) across the supply chain.</p>
        <p>In simple terms, filing a GST return means declaring how much tax you've collected from customers and how much you've already paid to your suppliers. The balance — if any — becomes your payable tax to the government.</p>
        <p>Each GST return consists of the following components:</p>
        <ul class="list-unstyled">
            <li><img src="https://static.wixstatic.com/media/42bffb_b82f85d02f1e4bea948731aa77c15256~mv2.png/v1/fill/w_50,h_50,al_c,q_85,usm_0.66_1.00_0.01,enc_avif,quality_auto/tax.png" alt="tax" class="img-fluid d-inline mr-3"> Input Tax Credit (GST Paid on Purchases): The credit that can be claimed against the tax you paid on purchases, helping avoid double taxation.</li>
            <li><img src="https://static.wixstatic.com/media/42bffb_e167020af0c44dbaa50a6ffa8a771861~mv2.png/v1/fill/w_50,h_50,al_c,q_85,usm_0.66_1.00_0.01,enc_avif,quality_auto/sales.png" alt="sales" class="img-fluid d-inline mr-3"> Output GST (On Sales): The total tax collected from customers on outward supplies.</li>
            <li><img src="https://static.wixstatic.com/media/42bffb_ca2dbc87c9cf4d45b5efe57c8b816e4f~mv2.png/v1/fill/w_50,h_50,al_c,q_85,usm_0.66_1.00_0.01,enc_avif,quality_auto/save.png" alt="save" class="img-fluid d-inline mr-3"> Purchases (Inward Supplies): Records purchases made from registered suppliers and the GST paid on them.</li>
            <li><img src="https://static.wixstatic.com/media/42bffb_a075190b959b4c94aa84952facee8cfa~mv2.png/v1/fill/w_50,h_50,al_c,q_85,usm_0.66_1.00_0.01,enc_avif,quality_auto/checklist.png" alt="checklist" class="img-fluid d-inline mr-3"> Sales (Outward Supplies): Lists all invoices for goods and services sold during the period, including the applicable GST charged.</li>
        </ul>
