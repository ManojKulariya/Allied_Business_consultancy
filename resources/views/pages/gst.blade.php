        <h3>Eligibility Criteria</h3>
        <p>Under the current GST law, the filing requirement applies to:</p>
        <ul>
            <li>All registered businesses with GSTINs.</li>
            <li>Individuals and proprietors providing taxable goods or services.</li>
            <li>Freelancers and professionals crossing the prescribed turnover limits.</li>
            <li>E-commerce operators who collect tax at source (TCS).</li>
            <li>Input Service Distributors (ISDs) distributing tax credits to multiple branches.</li>
            <li>Non-resident taxable persons supplying goods or services in India.</li>
        </ul>
