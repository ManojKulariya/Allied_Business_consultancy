    <section class="benefits-llp py-5">
        <h2>Benefits of Registering a Limited Liability Partnership</h2>
        <div class="row">
            <div class="col-md-3">
                <div class="card">
                    <div class="card-body">
                        <h5 class="card-title">Independent Legal Status</h5>
                        <p class="card-text">The registration of a limited liability partnership establishes a distinct legal existence from its participants. The LLP Act of 2008, permits the company to enter into agreements with other organisations, file lawsuits, own property, and borrow money under the LLP's name. It gives the company the freedom to run autonomously and forever, regardless of partner changes or deaths.</p>
                    </div>
                </div>
            </div>
            <div class="col-md-3">
                <div class="card">
                    <div class="card-body">
                        <h5 class="card-title">Limited Liabilities of Partners</h5>
                        <p class="card-text">Having a distinct legal personality is the main benefit of registering as a Limited Liability Partnership rather than a Partnership Firm. As a result, LLP offers its partners a restricted liability benefit. According to the LLP agreement, the partners' liability is limited to the capital contribution in the event of a business loss or insolvency. Furthermore, no partner is liable for the carelessness or wrongdoing of the other partners.</p>
                    </div>
                </div>
            </div>
            <div class="col-md-3">
                <div class="card">
                    <div class="card-body">
                        <h5 class="card-title">Operational Flexibility</h5>
                        <p class="card-text">An LLP's functioning framework, including the partners' rights and responsibilities, are clarified by the LLP Agreement, a deed between the partners. LLPs usually have a "Designated Member" in charge of day-to-day management. Both individuals and already-existing businesses may be members. Additionally, this structure makes it possible to specify the partners' duties and obligations precisely. It might also aid in safeguarding the partner's interests in the event that they are lost due to an illegal act committed by another partner.</p>
                    </div>
                </div>
            </div>
            <div class="col-md-3">
                <div class="card">
                    <div class="card-body">
                        <h5 class="card-title">Reduced Need for Compliance</h5>
                        <p class="card-text">An LLP is subject to less compliance standards than a private limited company. Until a specific amount of turnover or contribution is reached, there is no mandatory audit need. LLPs are not subject to the same regulations as corporations regarding board meetings, statutory meetings, etc. It is a cost-effective formation type to maintain because professional services for compliance are usually offered at lower costs than those for businesses.</p>
                    </div>
                </div>
            </div>
        </div>
    </section>

