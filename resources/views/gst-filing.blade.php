    <section id="what-is-gst-return" class="mb-5">
        <h2>What is a GST Return?</h2>
        <p>A GST Return is a detailed financial statement that captures every transaction made by a business during a tax period. It includes details of sales, purchases, taxes collected on sales, and taxes paid on inputs. The Government uses this information to calculate your net GST liability and ensure proper flow of Input Tax Credit (ITC) across the supply chain.</p>
        <p>In simple terms, filing a GST return means declaring how much tax you've collected from customers and how much you've already paid to your suppliers. The balance — if any — becomes your payable tax to the government.</p>
        <img src="https://static.wixstatic.com/media/ef5ab1_8f38f88cb08047008066d3f78ed1f14e~mv2.png/v1/fill/w_662,h_1276,al_c,q_90,usm_0.66_1.00_0.01,enc_avif,quality_auto/Rajkumar-Rao4.png" alt="Rajkumar Rao" class="img-fluid">
        <p>Each GST return consists of the following components:</p>
        <ul>
            <li><img src="https://static.wixstatic.com/media/42bffb_b82f85d02f1e4bea948731aa77c15256~mv2.png/v1/fill/w_50,h_50,al_c,q_85,usm_0.66_1.00_0.01,enc_avif,quality_auto/tax.png" alt="Tax"> Input Tax Credit (GST Paid on Purchases): The credit that can be claimed against the tax you paid on purchases, helping avoid double taxation.</li>
            <li><img src="https://static.wixstatic.com/media/42bffb_e167020af0c44dbaa50a6ffa8a771861~mv2.png/v1/fill/w_50,h_50,al_c,q_85,usm_0.66_1.00_0.01,enc_avif,quality_auto/sales.png" alt="Sales"> Purchases (Inward Supplies): Records purchases made from registered suppliers and the GST paid on them.</li>
            <li><img src="https://static.wixstatic.com/media/42bffb_ca2dbc87c9cf4d45b5efe57c8b816e4f~mv2.png/v1/fill/w_50,h_50,al_c,q_85,usm_0.66_1.00_0.01,enc_avif,quality_auto/save.png" alt="Save"> Output GST (On Sales): The total tax collected from customers on outward supplies.</li>
            <li><img src="https://static.wixstatic.com/media/42bffb_a075190b959b4c94aa84952facee8cfa~mv2.png/v1/fill/w_50,h_50,al_c,q_85,usm_0.66_1.00_0.01,enc_avif,quality_auto/checklist.png" alt="Checklist"> Sales (Outward Supplies): Lists all invoices for goods and services sold during the period, including the applicable GST charged.</li>
        </ul>
        <p>Proper GST return filing ensures that your tax liability is calculated correctly and that your Input Tax Credit is not blocked due to mismatched data.</p>
